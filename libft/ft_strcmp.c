#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;
	char	*str1;
	char	*str2;

	i = 0;
	str1 = (char *)s1;
	str2 = (char *)s2;
	while (str1[i] != '\0' && str2[i] == str1[i])
	{
		i++;
	}
	return (str1[i] - str2[i]);
}
